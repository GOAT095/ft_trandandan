const ballRadius = 5.0;
const ballDimensions = [ballRadius, ballRadius, ballRadius];
const ballColor = [223.0 /255.0, 1.0, 79.0 / 255.0, 1.0];
let   ballPosition = [0.0, 0.0, 0.0];
let   ballDirection = [1.0, 0.0, 0.0];
//ballDirection = normalizeV3(ballDirection);

const fieldDimensions = [512.0, 0.1, 256.0];
const fieldPosition = [0.0, -ballRadius, 0.0];
const fieldColor = [80.0 / 255.0, 200.0 / 255.0, 120.0 / 255.0, 1.0];

const middleLineDimensions = [5.0, 0.1, 256.0];
const middleLinePosition = [0.0, -ballRadius, 0.0];

const paddleDimensions = [5.0, 5.0, 32.0];

const player1Color = [227.0 / 255.0, 38.0 / 255.0, 51.0 / 255.0, 1.0];
const player1StartingPosition = [500.0 / 2.0, 0.0, 0.0];

const player2StartingPosition = [-500.0 / 2.0, 0.0, 0.0];
const player2Color = [0.0, 56.0 / 255.0, 123.0 / 255.0, 1.0];


const camPosition = [0.0, 250.0, -120.0];
const cam2Position = [420.0, 200.0, 0.0];
const classicPosition = [0.0, 200.0, 0.0];
const camAt = [0.0, 0.0, 0.0];
const worldUp = [0.0, 1.0, 0.0];
let   camChosen = 0;

const FIELD_OF_VIEW = toRadians(80.0);
const NEAR = 0.1;
const FAR = 1000.0;

const paddelMovementStep = 0.3;
const ballMovementStep = 7.0;


function render()
{
    const currentTime = performance.now();
    
    const deltaTime = currentTime - startTime;
    // Update(currentTime - startTime);
    startTime = currentTime;

    if (Movement == MOVEMENT_UP)
    {
        position[2] += (paddelMovementStep * deltaTime);
        if (position[2] + (paddleDimensions[2] / 2.0) > (fieldDimensions[2] / 2.0))
            position[2] = (fieldDimensions[2] / 2.0) - (paddleDimensions[2] / 2.0);

    }
    if (Movement == MOVEMENT_DOWN)
    {
        position[2] -= (paddelMovementStep * deltaTime);
        if (position[2] - (paddleDimensions[2] / 2.0) < -(fieldDimensions[2] / 2.0))
            position[2] = (-fieldDimensions[2] / 2.0) - (-paddleDimensions[2] / 2.0);

    }

    if (Movement2 == MOVEMENT_UP)
    {
        position2[2] += (paddelMovementStep * deltaTime);
        if (position2[2] + (paddleDimensions[2] / 2.0) > (fieldDimensions[2] / 2.0))
            position2[2] = (fieldDimensions[2] / 2.0) - (paddleDimensions[2] / 2.0);

    }
    if (Movement2 == MOVEMENT_DOWN)
    {
        position2[2] -= (paddelMovementStep * deltaTime);
        if (position2[2] - (paddleDimensions[2] / 2.0) < -(fieldDimensions[2] / 2.0))
            position2[2] = (-fieldDimensions[2] / 2.0) - (-paddleDimensions[2] / 2.0);
    }

    if (startGame)
    {
        ballPosition = addV3(mulV3f(ballDirection, ballMovementStep), ballPosition);
        if (ballPosition[0] < (-fieldDimensions[0] / 2.0) - (3.0 * ballRadius) ||
            ballPosition[0] > (fieldDimensions[0] / 2.0) + (3.0 * ballRadius))
        {
            startGame = false;
            ballDirection = [1.0, 0.0, 1.0];
            ballDirection = normalizeV3(ballDirection);
            ballPosition = [0.0, 0.0, 0.0];
        }
        if (ballPosition[2] + ballRadius > fieldDimensions[2] / 2.0)
        {
            ballPosition[2] = (fieldDimensions[2] / 2.0) - ballRadius;
            ballDirection[2] *= -1.0;
        }
        if (ballPosition[2] - ballRadius < -fieldDimensions[2] / 2.0)
        {
            ballPosition[2] = (-fieldDimensions[2] / 2.0) + ballRadius;
            ballDirection[2] *= -1.0;
        }
        if (dotV3(ballDirection, [1.0, 0.0, 0.0]) > 0)
        {
            //test intersection with paddel 1 only if the ball is going in the direction of the paddel
            // console.log("red");
            const player1Position = addV3(player1StartingPosition, position);
            const nearZ = Math.max(player1Position[2] - (paddleDimensions[2] / 2.0), Math.min(ballPosition[2], player1Position[2] + (paddleDimensions[2] / 2.0)));
            const nearX = Math.max(player1Position[0] - (paddleDimensions[0] / 2.0), Math.min(ballPosition[0], player1Position[0] + (paddleDimensions[0] / 2.0)));
            const nearestPointOnPaddle = [nearX, 0.0, nearZ];
            const ballNearestVec = subV3(nearestPointOnPaddle, ballPosition);
            const dot = dotV3(ballNearestVec, ballNearestVec);
            if (dot < ballRadius * ballRadius)
            {
                // ballPosition = addV3(nearestPointOnPaddle, mulV3f(ballDirection, -ballRadius));
                if (nearestPointOnPaddle[0] > player1Position[0] + (paddleDimensions[0] * 0.5))
                    nearestPointOnPaddle[0] = player1Position[0] + (paddleDimensions[0] * 0.5);
                player1Position[0] += 5.0;
                const reflectionVec = subV3(nearestPointOnPaddle, player1Position);
                ballDirection = reflectionVec;
                ballDirection = normalizeV3(ballDirection);
            }
        }
        else
        {
            //test intersection with paddel 2 the same way of paddel 1
            // console.log("blue");
            const player2Position = addV3(player2StartingPosition, position2);
            const nearZ = Math.max(player2Position[2] - (paddleDimensions[2] / 2.0), Math.min(ballPosition[2], player2Position[2] + (paddleDimensions[2] / 2.0)));
            const nearX = Math.max(player2Position[0] - (paddleDimensions[0] / 2.0), Math.min(ballPosition[0], player2Position[0] + (paddleDimensions[0] / 2.0)));
            const nearestPointOnPaddle = [nearX, 0.0, nearZ];
            const ballNearestVec = subV3(nearestPointOnPaddle, ballPosition);
            const dot = dotV3(ballNearestVec, ballNearestVec);
            if (dot < ballRadius * ballRadius)
            {
                // ballPosition = addV3(nearestPointOnPaddle, mulV3f(ballDirection, -ballRadius));
                if (nearestPointOnPaddle[0] > player2Position[0] + (paddleDimensions[0] * 0.5))
                    nearestPointOnPaddle[0] = player2Position[0] + (paddleDimensions[0] * 0.5);
                player2Position[0] -= 5.0;
                const reflectionVec = subV3(nearestPointOnPaddle, player2Position);
                ballDirection = reflectionVec;
                ballDirection = normalizeV3(ballDirection);
            }
        }
    }

    glContext.viewport(0, 0, canvas.width, canvas.height);
    glContext.clearColor(220.0 / 255.0, 220.0 / 255.0, 220.0 / 255.0, 1.0);
    glContext.clear(glContext.COLOR_BUFFER_BIT);

    let canvasRatio = canvas.width / canvas.height;
    let projection = perspectiveLH(FIELD_OF_VIEW, canvasRatio, NEAR, FAR);
    let view;
    if (camChosen == 0)
        view = lookAtLH(camPosition, camAt, worldUp);
    else if (camChosen == 1)
        view = lookAtLH(cam2Position, camAt, worldUp);

    //DRAW the middle line of the field
    let projectionLoc = glContext.getUniformLocation(program, 'projection');
    let viewLoc = glContext.getUniformLocation(program, 'view');
    let modelLoc = glContext.getUniformLocation(program, 'model');
    let objColorLoc = glContext.getUniformLocation(program, "objColor");

    let middleLineModel = scaleM4x4(middleLineDimensions[0], middleLineDimensions[1], middleLineDimensions[2]);
    middleLineModel = mat4x4Mul(middleLineModel, translateM4x4(middleLinePosition[0], middleLinePosition[1], middleLinePosition[2]));
    let middleLineColor = [1.0, 1.0, 1.0, 1.0];

    glContext.uniformMatrix4fv(projectionLoc, false, projection);
    glContext.uniformMatrix4fv(viewLoc, false, view);
    glContext.uniformMatrix4fv(modelLoc, false, middleLineModel);
    glContext.uniform4fv(objColorLoc, middleLineColor);

    glContext.drawArrays(glContext.TRIANGLES, sphereSize, 36);
    
    //Draw the Field
    let fieldModel = scaleM4x4(fieldDimensions[0], fieldDimensions[1], fieldDimensions[2]);
    fieldModel = mat4x4Mul(fieldModel, translateM4x4(fieldPosition[0], fieldPosition[1], fieldPosition[2]));

    glContext.uniformMatrix4fv(projectionLoc, false, projection);
    glContext.uniformMatrix4fv(viewLoc, false, view);
    glContext.uniformMatrix4fv(modelLoc, false, fieldModel);
    glContext.uniform4fv(objColorLoc, fieldColor);

    glContext.drawArrays(glContext.TRIANGLES, sphereSize, 36);

    // Drawing the left paddle

    let model = scaleM4x4(paddleDimensions[0], paddleDimensions[1], paddleDimensions[2]);
    model = mat4x4Mul(model, translateM4x4(player1StartingPosition[0] + position[0], player1StartingPosition[1] + position[1], player1StartingPosition[2] + position[2]));
    
    glContext.uniformMatrix4fv(projectionLoc, false, projection);
    glContext.uniformMatrix4fv(viewLoc, false, view);
    glContext.uniformMatrix4fv(modelLoc, false, model);
    glContext.uniform4fv(objColorLoc, player1Color);

    //Drawing the right paddle
    glContext.drawArrays(glContext.TRIANGLES, sphereSize, 36);
    let model2 = scaleM4x4(paddleDimensions[0], paddleDimensions[1], paddleDimensions[2]);
    model2 = mat4x4Mul(model2, translateM4x4(player2StartingPosition[0] + position2[0], player2StartingPosition[1] + position2[1], player2StartingPosition[2] + position2[2]));

    glContext.uniformMatrix4fv(projectionLoc, false, projection);
    glContext.uniformMatrix4fv(viewLoc, false, view);
    glContext.uniformMatrix4fv(modelLoc, false, model2);
    glContext.uniform4fv(objColorLoc, player2Color);
    
    glContext.drawArrays(glContext.TRIANGLES, sphereSize, 36);

    //Drawing the Ball
    let ballModel = scaleM4x4(ballDimensions[0], ballDimensions[1], ballDimensions[2]);
    ballModel = mat4x4Mul(ballModel, translateM4x4(ballPosition[0], ballPosition[1], ballPosition[2]));

    glContext.uniformMatrix4fv(projectionLoc, false, projection);
    glContext.uniformMatrix4fv(viewLoc, false, view);
    glContext.uniformMatrix4fv(modelLoc, false, ballModel);
    glContext.uniform4fv(objColorLoc, ballColor);

    glContext.drawArrays(glContext.TRIANGLES, 0, sphereSize);
    window.requestAnimationFrame(render);
}

