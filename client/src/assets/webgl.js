function addV3(left, right)
{
    return ([left[0] + right[0], left[1] + right[1], left[2] + right[2]]);
}

function subV3(left, right)
{
    return ([left[0] - right[0], left[1] - right[1], left[2] - right[2]]);
}

function mulV3(left, right)
{
    return ([left[0] * right[0], left[1] * right[1], left[2] * right[2]]);
}

function mulV3f(left, right)
{
    return ([left[0] * right, left[1] * right, left[2] * right]);
}

function dotV3(left, right)
{
    return ((left[0] * right[0]) + (left[1] * right[1]) + (left[2] * right[2]));
}

function crossV3(left, right)
{
    return ([(left[1] * right[2]) - (left[2] * right[1]),
    (left[2] * right[0]) - (left[0] * right[2]),
    (left[0] * right[1]) - (left[1] * right[0])
    ]);
}

function lenV3(vec)
{
    return (Math.sqrt(dotV3(vec, vec)));
}

function normalizeV3(vec)
{
    const len = lenV3(vec);
    return ([vec[0] / len, vec[1] / len, vec[2] / len]);
}

